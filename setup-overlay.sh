#!/usr/bin/env bash
# claude-code-multi-account — HOME-overlay setup for a secondary Claude Code account (macOS).
#
# Gives a secondary account its OWN $HOME (a symlink overlay of your real home) so it gets a
# fully separate macOS keychain + ~/.claude and runs *vanilla* claude with a real /login session
# — remote control, /usage, native toolchain, no "Claude API" label. Your primary account stays
# in your real $HOME. Both run simultaneously, isolated.
#
# Usage:  ./setup-overlay.sh <profile>          e.g.  ./setup-overlay.sh work
#
# Idempotent. Creates: ~/<profile>-home (overlay), a separate login keychain, a `claude-<profile>`
# wrapper, and a direnv shim. The only thing it adds to your REAL home is one keychain item (the
# overlay keychain's password) plus the wrapper/shim under ~/.local/bin and ~/.claude-shims.
set -euo pipefail

PROFILE="${1:-}"
[ -n "$PROFILE" ] || { echo "usage: $0 <profile>   (e.g. work)" >&2; exit 2; }
[ "$(uname -s)" = "Darwin" ] || { echo "macOS only — Linux/Windows don't need this (see README)." >&2; exit 2; }

REAL_HOME="$HOME"
OVERLAY="$REAL_HOME/${PROFILE}-home"
KC="$OVERLAY/Library/Keychains/login.keychain-db"
PW_SVC="claude-overlay-${PROFILE}"                       # personal-keychain item holding the overlay kc password
WRAPPER="$REAL_HOME/.local/bin/claude-${PROFILE}"
SHIM_DIR="$REAL_HOME/.claude-shims/${PROFILE}"
REAL_CLAUDE_BIN="$REAL_HOME/.local/bin/claude"          # stable symlink the installer maintains
[ -x "$REAL_CLAUDE_BIN" ] || { echo "claude not found at $REAL_CLAUDE_BIN" >&2; exit 2; }

echo "==> overlay home: $OVERLAY"

# 1. Overlay: symlink shared state -> real home; keep claude + keychain separate (real dirs).
mkdir -p "$OVERLAY/.claude" "$OVERLAY/Library/Keychains" "$OVERLAY/Library/Preferences" \
         "$OVERLAY/Library/Caches" "$OVERLAY/.local/state"
# Shared so the secondary account has your identical toolchain, shell, git/ssh, generic config.
SHARED=( .nvm .npmrc .npm .yarnrc .config .cache .gitconfig .gitignore_global .ssh
         .zshrc .zshenv .zprofile .bashrc .bash_profile .profile .p10k.zsh
         .tool-versions .asdf .rbenv .pyenv .cargo .rustup .lmstudio .orbstack .docker .aws )
for e in "${SHARED[@]}"; do
  [ -e "$REAL_HOME/$e" ] && [ ! -e "$OVERLAY/$e" ] && ln -s "$REAL_HOME/$e" "$OVERLAY/$e" || true
done
for e in bin share; do   # .local/bin (tool shims), .local/share (incl. the claude install); .local/state stays separate
  [ -e "$REAL_HOME/.local/$e" ] && [ ! -e "$OVERLAY/.local/$e" ] && ln -s "$REAL_HOME/.local/$e" "$OVERLAY/.local/$e" || true
done
[ -e "$REAL_HOME/Library/pnpm" ] && [ ! -e "$OVERLAY/Library/pnpm" ] && ln -s "$REAL_HOME/Library/pnpm" "$OVERLAY/Library/pnpm" || true
echo "    built (separate: .claude, .claude.json, Library/Keychains; everything else symlinked to your real home)"

# 2. Separate login keychain; password stashed in your PERSONAL login keychain (no plaintext file).
if [ ! -f "$KC" ]; then
  PW="$(openssl rand -base64 24)"
  HOME="$OVERLAY" security create-keychain -p "$PW" "$KC"
  HOME="$OVERLAY" security set-keychain-settings "$KC"            # no idle auto-lock, so token refresh keeps working
  HOME="$OVERLAY" security default-keychain -s "$KC"
  HOME="$OVERLAY" security list-keychains -s "$KC" "/Library/Keychains/System.keychain"
  HOME="$OVERLAY" security unlock-keychain -p "$PW" "$KC"
  security add-generic-password -U -s "$PW_SVC" -a "$USER" -w "$PW" -T /usr/bin/security
  echo "    created overlay keychain (password stored in your personal login keychain: $PW_SVC)"
else
  echo "    overlay keychain already present"
fi

# 3. Wrapper: claude-<profile>
mkdir -p "$(dirname "$WRAPPER")"
cat > "$WRAPPER" <<EOF
#!/bin/zsh
# Run claude as the "$PROFILE" account via its HOME overlay. Generated by setup-overlay.sh.
OVERLAY="$OVERLAY"
REAL_CLAUDE="$REAL_CLAUDE_BIN"
SELF="$WRAPPER"                              # absolute path of this wrapper, baked at generation
# Unlock the overlay keychain (its password lives in your personal login keychain).
PW="\$(security find-generic-password -s '$PW_SVC' -w 2>/dev/null)"
[ -n "\$PW" ] && security unlock-keychain -p "\$PW" "\$OVERLAY/Library/Keychains/login.keychain-db" 2>/dev/null
# Bridge keyring-based creds the overlay keychain can't see. gh keeps its token in the macOS
# keychain; read it NOW (HOME still real) and pass via env, which outranks the keyring.
if command -v gh >/dev/null 2>&1; then T="\$(gh auth token 2>/dev/null)"; [ -n "\$T" ] && export GH_TOKEN="\$T"; fi
export HOME="\$OVERLAY"
export PATH="\$HOME/.local/bin:\$PATH"      # \$HOME/.local/bin -> real dir; silences /doctor's PATH check
unset CLAUDE_CONFIG_DIR CLAUDE_CODE_OAUTH_TOKEN
# If launched inside a cmux session, route through cmux's wrapper so the work session shows in the
# cmux sidebar AND survives cmux auto-resume. cmux persists CMUX_CUSTOM_CLAUDE_PATH per workspace
# but NOT HOME, and replays that path under the real (personal) HOME — so we must capture THIS
# wrapper as that path. Resume then re-enters us and re-establishes the overlay HOME before handing
# off to the real binary. The == \$SELF guard collapses this to exactly one cmux-wrap pass ending at
# the real claude (no loop, no double hook injection): fresh launch sets it and execs cmux-wrap,
# which execs us again -> now == \$SELF -> real claude; resume replays with it already set -> same tail.
CMUX_WRAP="/Applications/cmux.app/Contents/Resources/bin/cmux-claude-wrapper"
if [ -n "\${CMUX_SURFACE_ID:-}" ] && [ -x "\$CMUX_WRAP" ]; then
  if [ "\${CMUX_CUSTOM_CLAUDE_PATH:-}" = "\$SELF" ]; then
    export CMUX_CUSTOM_CLAUDE_PATH="\$REAL_CLAUDE"   # cmux already re-entered us; HOME overlaid -> real claude
    exec "\$REAL_CLAUDE" "\$@"
  fi
  export CMUX_CUSTOM_CLAUDE_PATH="\$SELF"            # make cmux capture THIS wrapper -> resume re-overlays HOME
  exec "\$CMUX_WRAP" "\$@"
fi
exec "\$REAL_CLAUDE" "\$@"
EOF
chmod +x "$WRAPPER"
echo "    wrapper:  $WRAPPER"

# 4. direnv shim: makes plain `claude` resolve to the wrapper inside a work tree (see README).
mkdir -p "$SHIM_DIR"
cat > "$SHIM_DIR/claude" <<EOF
#!/bin/zsh
exec "$WRAPPER" "\$@"
EOF
chmod +x "$SHIM_DIR/claude"
echo "    shim:     $SHIM_DIR/claude"

# 5. Non-interactive verification (the interactive /login + /status checks come after).
echo
echo "==> verify"
echo "    node resolves to: $(command -v node || echo '?') — PATH-based, unaffected by HOME (no reinstall)"
echo "    overlay keychain isolated: $(HOME="$OVERLAY" security find-generic-password -s 'Claude Code-credentials' >/dev/null 2>&1 && echo 'has a login' || echo 'empty (expected until you /login)')"
echo "    your personal default keychain unchanged: $(security default-keychain | tr -d ' \"')"

cat <<NEXT

Next:
  1. Log in the "$PROFILE" account (one-time, interactive):
       claude-$PROFILE        # then run /login and pick the $PROFILE account
  2. Auto-route by directory — in each "$PROFILE" work tree:
       echo 'PATH_add "$SHIM_DIR"' > .envrc && direnv allow
     Now \`claude\` / \`clauded\` in that tree -> $PROFILE; everywhere else -> your primary account.
  3. Confirm in-session: /status (real account, not "Claude API"), /usage (meters), /remote-control.

Migrating an existing token-profile's config + transcripts into the overlay? See README -> "Migrating".
NEXT
